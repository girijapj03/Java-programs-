class Incrementoperatordemo{
public static void main(String args[]){
int p=8;
int q=5;
int r=q++;
System.out.println(r++);
System.out.println(++q);
System.out.println(p++);
System.out.println(r);
System.out.println(p);
System.out.println(q);
System.out.println(q--);
System.out.println(--q);


}
}
